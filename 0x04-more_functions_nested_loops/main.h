#ifndef MAIN_H
#define MAIN_H

/**
 * _isupper - checks if a character c is uppercase or lowercase
 * @c: character
 * Return: integer
 */

int _isupper(int c);


/**
 * _isdigit - checks if c is a digit
 * @c: an integer to be checked
 * Return: 1 if c is a digit and 0 it not
 */

int _isdigit(int c);


/**
 * mul - multiply two integers
 * @a: an integer to be multiplied
 * @b: an integer to be multiplied
 * Return: integer
 */

int mul(int a, int b);

#endif
